/**
 * 成员活动状态
 *
 * @author fwj
 * @date 11-9-29
 */
public enum MemberState
{
    /**
     * 申请中
     */
    applying,

    /**
     * 参与
     */
    participation,

    /**
     * 未通过
     */
    fail,

    /**
     * 邀请
     */
    invite,

    /**
     * 未参与
     */
    nonparticipation
}
