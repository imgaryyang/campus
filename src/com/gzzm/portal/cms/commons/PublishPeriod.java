package com.gzzm.portal.cms.commons;

/**
 * 发布周期
 *
 * @author camel
 * @date 13-12-12
 */
public enum PublishPeriod
{
    /**
     * 不定期
     */
    NULL,

    /**
     * 每周发布一次
     */
    WEEK,

    /**
     * 每月发布一次
     */
    MONTH,

    /**
     * 每季度发布一次
     */
    QUARTER,

    /**
     * 每半年发布一次
     */
    HALF_YEAR,

    /**
     * 每年发布一次
     */
    YEAR
}
